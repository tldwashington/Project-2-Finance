## Scenario:

-- You’ve been brought in as a junior data analyst for a regional bank. Your task is to analyze customer transaction data, identify trends in account activity, and help flag potential communication issues for client outreach. You’ll work with three tables: Customer, Account, and Transaction.



-- Question 1: Identify the customer with the highest total transaction amount across all their accounts. 


-- Question 2: Find the total transaction amount per account, and filter for accounts where the total is greater than $1,000.

-- Question 3: Create a report showing each customer's full name. 


-- Question 4: Provide a list of distinct transaction types recorded in the system. 

-- Question 5: Show a list of all customers with their account types and balances, even if the customer doesn't have an email address on file. 
